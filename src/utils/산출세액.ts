/**
 * 총 급여 기준으로 실제 내야 할 소득세(산출세액)를 계산합니다.
 *
 * 과세표준에 따라 대한민국 소득세 누진세율을 적용합니다.
 *
 * @param {number} 과세표준 - 소득세 계산 기준이 되는 과세표준 금액 (원 단위)
 * @returns {number} 산출세액 - 계산된 소득세 금액 (원 단위)
 *
 */
export default function 산출세액(과세표준: number): number {
  let 세액 = 0;

  if (과세표준 <= 1_400_0000) {
    // 1,400만원 이하
    세액 = 과세표준 * 0.06;
  } else if (과세표준 <= 5_000_0000) {
    // 1,400만원 초과 ~ 5,000만원 이하
    세액 = 840_000 + (과세표준 - 1_400_0000) * 0.15;
  } else if (과세표준 <= 8_800_0000) {
    // 5,000만원 초과 ~ 8,800만원 이하
    세액 = 6_240_000 + (과세표준 - 5_000_0000) * 0.24;
  } else if (과세표준 <= 15_000_0000) {
    // 8,800만원 초과 ~ 1억5천만원 이하
    세액 = 15_360_000 + (과세표준 - 8_800_0000) * 0.35;
  } else if (과세표준 <= 30_000_0000) {
    // 1억5천만원 초과 ~ 3억원 이하
    세액 = 37_060_000 + (과세표준 - 15_000_0000) * 0.38;
  } else if (과세표준 <= 50_000_0000) {
    // 3억원 초과 ~ 5억원 이하
    세액 = 94_060_000 + (과세표준 - 30_000_0000) * 0.4;
  } else if (과세표준 <= 100_000_0000) {
    // 5억원 초과 ~ 10억원 이하
    세액 = 174_060_000 + (과세표준 - 50_000_0000) * 0.42;
  } else {
    // 10억원 초과
    세액 = 384_060_000 + (과세표준 - 100_000_0000) * 0.45;
  }

  return 세액;
}
