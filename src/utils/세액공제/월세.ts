/**
 * 월세 세액공제
 *
 * [한도]
 * - 연 1,000만원까지 공제 가능
 *
 * [공제율 조건]
 * - 총급여 5,500만원 이하 & 종합소득금액 4,500만원 이하 → 17%
 * - 총급여 5,500만원 초과 ~ 8,000만원 이하 & 종합소득금액 7,000만원 이하 → 15%
 * - 그 외 → 공제 없음
 */
export default function 월세세액공제(
  연간월세액: number,
  총급여: number,
  종합소득금액: number
): number {
  const 한도적용월세 = Math.min(연간월세액, 10_000_000);

  let 공제율 = 0;

  if (총급여 <= 55_000_000 && 종합소득금액 <= 45_000_000) {
    공제율 = 0.17;
  } else if (
    총급여 > 55_000_000 &&
    총급여 <= 80_000_000 &&
    종합소득금액 <= 70_000_000
  ) {
    공제율 = 0.15;
  }

  return 한도적용월세 * 공제율;
}
