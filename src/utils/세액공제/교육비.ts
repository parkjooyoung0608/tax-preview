/**
 * [교육비 세액공제 규정]
 * - 본인: 전액 공제, 공제율 15%
 *
 * - 초·중·고 학생 (1명당)
 *   한도: 연 300만원
 *   공제율: 15%
 *
 * - 대학생 (1명당)
 *   한도: 연 900만원
 *   공제율: 15%
 *
 * - 대학원생: 공제대상 아님
 *
 * - 장애인 특수교육비 (직계존속 포함)
 *   한도: 없음
 *   공제율: 15%
 */
export default function 교육비세액공제(
  본인교육비: number,
  초중고교육비: number,
  대학생교육비: number,
  장애인특수교육비: number
): number {
  const 본인공제 = 본인교육비 * 0.15;
  const 초중고공제 = Math.min(초중고교육비, 3_000_000) * 0.15;
  const 대학생공제 = Math.min(대학생교육비, 9_000_000) * 0.15;
  const 장애인공제 = 장애인특수교육비 * 0.15;

  return 본인공제 + 초중고공제 + 대학생공제 + 장애인공제;
}
