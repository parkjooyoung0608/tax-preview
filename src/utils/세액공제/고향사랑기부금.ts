/**
 * 고향사랑기부금 세액공제 계산
 *
 * [공제율 조건]
 * - 10만원 이하: 100% 세액공제
 * - 10만원 초과 ~ 500만원 이하: 초과분의 16.5% 세액공제
 * - 연간 500만원 한도
 */
export default function 고향사랑기부금(기부금액: number): number {
  const 한도적용금액 = Math.min(기부금액, 5_000_000);

  let 세액공제 = 0;

  if (한도적용금액 <= 100_000) {
    세액공제 = 한도적용금액; // 10만원까지 전액 공제
  } else {
    세액공제 = 100_000 + (한도적용금액 - 100_000) * 0.165; // 10만원 초과분 16.5%
  }

  return 세액공제;
}
