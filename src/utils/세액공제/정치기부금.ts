/**
 * 정치기부금 (정치후원금) 세액공제 계산 (2025년 기준)
 *
 * [공제율 조건]
 * - 10만원 이하: 기부금액의 100/110 (≈90.9%) 공제
 * - 10만원 초과 ~ 3,000만원 이하: 초과분 15% 세액공제
 * - 3,000만원 초과: 초과분 25% 세액공제
 * - 한도: 근로소득금액의 100% (별도 고려 필요)
 */
export default function 정치기부금(기부금액: number): number {
  const 기부한도금액 = Math.max(0, 기부금액); // 음수 방지용

  let 세액공제 = 0;

  if (기부한도금액 <= 100_000) {
    세액공제 = 기부한도금액 * (100 / 110); // 100/110 환급
  } else if (기부한도금액 <= 30_000_000) {
    세액공제 = 100_000 * (100 / 110) + (기부한도금액 - 100_000) * 0.15;
  } else {
    세액공제 =
      100_000 * (100 / 110) +
      (30_000_000 - 100_000) * 0.15 +
      (기부한도금액 - 30_000_000) * 0.25;
  }

  return 세액공제;
}
