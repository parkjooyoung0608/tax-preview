/**
 * [보험료 세액공제 규정]
 * - 보장성보험 (생명/상해보험 등)
 *   한도: 연 100만원
 *   공제율: 12%
 *
 * - 장애인전용 보장성보험
 *   한도: 연 100만원
 *   공제율: 15%
 */
export default function 보험료세액공제(
  보장성보험: number,
  장애인보험: number
): number {
  const 보장성보험공제 = Math.min(보장성보험, 1_000_000) * 0.12;
  const 장애인보험공제 = Math.min(장애인보험, 1_000_000) * 0.15;
  return 보장성보험공제 + 장애인보험공제;
}
