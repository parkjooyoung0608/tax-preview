/**
 * [기부금 세액공제 규정]
 *
 * - 정치자금 기부금
 *   한도: 근로소득금액 전액
 *   공제율:
 *     10만원 이하: (100/110)
 *     10만원 초과: 15% (3천만원 초과분 25%)
 *
 * - 고향사랑 기부금
 *   한도: 연 500만원
 *   공제율:
 *     10만원 이하: (100/110)
 *     10만원 초과: 15%
 *
 * - 특례기부금, 우리사주조합기부금, 일반기부금, 종교단체기부금
 *   한도: 근로소득금액 일정 비율
 *   공제율:
 *     1천만원 이하: 15%
 *     1천만원 초과 ~ 3천만원 이하: 30%
 *     3천만원 초과: 40% (2024년 한시 적용)
 */
export function 기부금세액공제(
  정치자금: number,
  고향사랑: number,
  특례기부금: number,
  우리사주조합: number,
  일반기부금: number,
  종교단체기부금: number
): number {
  let 공제액 = 0;

  // 정치자금 기부금
  if (정치자금 <= 100_000) {
    공제액 += 정치자금 * (100 / 110);
  } else {
    공제액 += 100_000 * (100 / 110);
    공제액 += (정치자금 - 100_000) * 0.15;
  }

  // 고향사랑 기부금
  if (고향사랑 <= 100_000) {
    공제액 += 고향사랑 * (100 / 110);
  } else {
    공제액 += 100_000 * (100 / 110);
    공제액 += (고향사랑 - 100_000) * 0.15;
  }

  // 기타 기부금
  const 기타기부금 = 특례기부금 + 우리사주조합 + 일반기부금 + 종교단체기부금;
  if (기타기부금 <= 10_000_000) {
    공제액 += 기타기부금 * 0.15;
  } else if (기타기부금 <= 30_000_000) {
    공제액 += 10_000_000 * 0.15 + (기타기부금 - 10_000_000) * 0.3;
  } else {
    공제액 +=
      10_000_000 * 0.15 + 20_000_000 * 0.3 + (기타기부금 - 30_000_000) * 0.4;
  }

  return 공제액;
}
